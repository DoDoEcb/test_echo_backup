<h3>Overview</h3>
<b><u>What Is OpenEx?</u></b>
	<div class="aboutlist">
		<li>OpenEx is an <a href="https://github.com/r3wt/openex.git">opensource(Link)</a> cryptocurrency exchange platform, 
		created by Garrett Morris(r3wt) and Justin Gillett(justin7674).</li>
		<br/>
	</div>

<b><u>Why OpenSource?</u></b>
	<div class="aboutlist">
		<li>The Open Source nature of OpenEx provides hands on access to programming and alt coin enthusiasts who wish to contribute to the project, 
		whether it be making new features, fixing security issues, or submitting bug reports.</li>
		<br/>
	</div>
	
<b><u>How Can I Contribute?</u></b>
	<div class="aboutlist">
		<li> you can contribute by <a href="https://github.com/r3wt/openex.git">forking the code(Link)</a> on 
		github and adding your own features or fixing bugs/security issues. Just submit a pull request and we will take over from there. 
		from time to time we may offer bounties for bug fixes, and especially security fixes.</li>
		</br>
	</div>
	
<b><u>How much does OpenEx make in commissions?</u></b>
	<div class="aboutlist">
		<li>OpenEx charges a trade fee of 0.5% and a withdrawal fee of 0.1%. In the future, this will be changed to a fairer system where the percentage is roughly double the network transaction fee of each coin.</li>
	</div>
	
