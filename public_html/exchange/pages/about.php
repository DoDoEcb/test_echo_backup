<b><u>Does OpenEx Offer Fee Shares?</u></b>
	<div class="aboutlist">
		<li>Yes, OpenEx has and will continue to offer fee shares.</li>
		<li>Details: 1000 publicly tradeable shares, valued at 20% of trading fee's.</li>
		<li>These shares are non dilutable, meaning that only 1000 publicly tradeable fee shares will ever exist.</li>
		<br/>
	</div>
	
<b><u>I purchased Fee Shares from the bitcointalk thread, will they be credited to me on CryptoStocks?</u></b>
	<div class="aboutlist">
		<li>Yes, we have been in contact with the staff of cryptocoin stocks and all valid shares will be credited. When the procedure for this operation is handed down to me, i will report it back to the community so you can start the transfer process.</li>
		<br/>
	</div>
	
<b><u>Where Can I buy Fee shares?</u></b>
	<div class="aboutlist">
		<li>Fee shares are temporarily unavailable while our proposal awaits approval with CryptoStocks.com. They should be available for purchase at a starting rate of .025 btc in the near future.</li>
		<br/>
	</div>
